let currentJob = "";
let currentQuestionIndex = 0;
let score = 0;
const maxScore = 100;

function startInterview(job) {
    currentJob = job;
    currentQuestionIndex = 0;
    score = 0;
    
    showQuestion();
}

function showQuestion() {
    if (currentQuestionIndex < questions[currentJob].length) {
        let questionObj = questions[currentJob][currentQuestionIndex];
        document.getElementById("question").innerText = questionObj.q;
        
        let choicesDiv = document.getElementById("choices");
        choicesDiv.innerHTML = "";
        
        questionObj.answers.forEach((answer, index) => {
            let label = document.createElement("label");
            let radio = document.createElement("input");
            radio.type = "radio";
            radio.name = "answer";
            radio.value = index;
            radio.onclick = () => document.getElementById("submit-radio").style.display = "block";
            
            label.appendChild(radio);
            label.appendChild(document.createTextNode(answer));
            choicesDiv.appendChild(label);
            choicesDiv.appendChild(document.createElement("br"));
        });
        
        let submitButton = document.createElement("button");
        submitButton.id = "submit-radio";
        submitButton.innerText = "Send Your Choose ";
        submitButton.style.display = "none";
        submitButton.onclick = submitRadioAnswer;
        
        choicesDiv.appendChild(submitButton);
    } else {
        document.getElementById("question").innerText = "تم الانتهاء من المقابلة!";
        document.getElementById("choices").innerHTML = "";
        document.getElementById("result").innerText = `نتيجتك: ${Math.min(score, maxScore)}/${maxScore} نقطة`;
        
        if (score >= 75) {
            addNextPageButton();
        }
        else{
            addRetryButton();
        }
    }

    }
 

function submitRadioAnswer() {
    let selectedRadio = document.querySelector('input[name="answer"]:checked');
    if (selectedRadio) {
        let index = parseInt(selectedRadio.value);
        let points = [1, 2, 3, 5]; // النقاط لكل إجابة
        score += points[index];
        
        currentQuestionIndex++;
        showQuestion();
    }
}

function submitAnswer() {
    let userAnswer = document.getElementById("text-answer").value.trim();
    
    if (userAnswer !== "") {
        let correctAnswer = findClosestAnswer(userAnswer, questions[currentJob][currentQuestionIndex].answers);
        let correctIndex = questions[currentJob][currentQuestionIndex].answers.indexOf(correctAnswer);
        
        let points = [1, 2, 3, 5]; // النقاط لكل إجابة
        score += points[correctIndex]; 
        
        document.getElementById("text-answer").value = "";
        currentQuestionIndex++;
        showQuestion();
    }
    else{
        alert("must enter answer")
    }
  
}

// دالة لمقارنة الإجابة المدخلة بأقرب إجابة من الاختيارات المتاحة
function findClosestAnswer(userAnswer, choices) {
    let closest = choices[0];
    let highestMatch = 0;

    choices.forEach(choice => {
        let match = similarity(userAnswer, choice);
        if (match > highestMatch) {
            highestMatch = match;
            closest = choice;
        }
    });

    return closest;
}

// دالة حساب التشابه بين النصوص
function similarity(str1, str2) {
    let words1 = str1.toLowerCase().split(" ");
    let words2 = str2.toLowerCase().split(" ");
    let commonWords = words1.filter(word => words2.includes(word));
    
    return commonWords.length / Math.max(words1.length, words2.length);
}

